
public class Persona
{
    public string Nombre;
    public string Apellidos;
    public string Genero;
    public int Edad;

    public Persona(string nombre, string apellidos, string genero, int edad)
    {
        Nombre = nombre;
        Apellidos = apellidos;
        Genero = genero;
        Edad = edad;
    }
    
    
        
}